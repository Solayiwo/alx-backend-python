#!/usr/bin/env python3
"""Define the add function with type annotations"""


def add(a: float, b: float) -> float:
    """
    Function that adds two floating-point numbers.

    Args:
        a (float): The first number to add.
        b (float): The second number to add.

    Returns:
        float: The sum of the two numbers.
    """
    return a + b
